package mobile.kecipir.com.checkout.Model;

public class VABankModel {

    private String img, title;

    public VABankModel(){

    }

    public VABankModel(String img, String title) {
        this.img = img;
        this.title = title;
    }

    public String getImg() {
        return img;
    }

    public String getTitle() {
        return title;
    }
}
